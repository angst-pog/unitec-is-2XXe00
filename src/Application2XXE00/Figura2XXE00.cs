public class Figura2XXE00
{
    // Perímetro de un Paralelogramo 2*(a + b)
    public static double CalcularPerimetroParalelogramo(double a , double b) => 2 * (a + b);

    // Área de un Paralelogramo b * c
    public static double CalcularAreaParalelogramo(double b , double c) => b * c;

    // Volumen de un Paralelogramo area * h
    public static double CalcularVolumenParalelogramo(double area, double h) => area * h;
}